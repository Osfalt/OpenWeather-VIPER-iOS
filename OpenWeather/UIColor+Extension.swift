import UIKit

extension UIColor {
    
    class var owBackground: UIColor {
        return UIColor(colorLiteralRed: 245/255, green: 245/255, blue: 245/255, alpha: 1.0)
    }
    
    class var owLightGray: UIColor {
        return UIColor(colorLiteralRed: 184/255, green: 184/255, blue: 184/255, alpha: 1.0)
    }
    
    class var owHighlight: UIColor {
        return UIColor(colorLiteralRed: 184/255, green: 184/255, blue: 184/255, alpha: 0.5)
    }
    
    class var owDarkBlue: UIColor {
        return UIColor(colorLiteralRed: 41/255, green: 115/255, blue: 177/255, alpha: 1.0)
    }
    
    class var owLightBlue: UIColor {
        return UIColor(colorLiteralRed: 197/255, green: 226/255, blue: 255/255, alpha: 1.0)
    }
    
    class var owVeryLightBlue: UIColor {
        return UIColor(colorLiteralRed: 242/255, green: 246/255, blue: 255/255, alpha: 1.0)
    }
    
    class var owLightRed: UIColor {
        return UIColor(colorLiteralRed: 255/255, green: 189/255, blue: 189/255, alpha: 1.0)
    }
    
    class var owRedText: UIColor {
        return UIColor(colorLiteralRed: 209/255, green: 72/255, blue: 54/255, alpha: 1.0)
    }
    
    class var owDarkRedText: UIColor {
        return UIColor(colorLiteralRed: 146/255, green: 54/255, blue: 54/255, alpha: 1.0)
    }
    
    class var owGreen: UIColor {
        return UIColor(colorLiteralRed: 28/255, green: 186/255, blue: 82/255, alpha: 1.0)
    }
}
